package if_Ejemplo;

public class Swich_Ejemplo {

	public static void main(String[] args) {
		int i = 9;
		
		switch (i) {
		case 0:
		System.out.println("i es 0");
		break;
		case 1:
			System.out.println("i es uno");
			break;
		case 2:
			System.out.println("i es 2");
			break;
			default:
				System.out.println("i es mayor a 2");
		}

	}

}
